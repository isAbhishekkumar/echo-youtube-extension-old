package dev.brahmkshatriya.echo.common.clients

/**
 * Enum representing the type of extension
 */
enum class ExtensionType {
    /**
     * A music extension that provides audio content
     */
    MUSIC,
    
    /**
     * A podcast extension that provides podcast content
     */
    PODCAST,
    
    /**
     * A radio extension that provides radio content
     */
    RADIO,
    
    /**
     * Other type of extension
     */
    OTHER
}